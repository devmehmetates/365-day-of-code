    }

    // MARK: - Table view data source


    
    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return flags.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "Flag", for: indexPath)
        cell.textLabel?.text = flags[indexPath.row].replacingOccurrences(of: "@3x.png", with: "").uppercased()
        cell.imageView?.image = UIImage(named: flags[indexPath.row])
        return cell
    }
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        if let vc = storyboard?.instantiateViewController(withIdentifier: "Detail") as? ViewController{
            vc.selectedFlag = flags[indexPath.row]
            vc.selectedName = flags[indexPath.row].replacingOccurrences(of: "@3x.png", with: "").uppercased()
            navigationController?.pushViewController(vc, animated: true)
        }
    }

